function cmd_a(playerid, index)
    if (index == nil) then
        return AddPlayerChat(playerid, "Usage: /a <animation>")
    end

    index = tonumber(index)

    if index < 0 or index > 165 then
        return AddPlayerChat(playerid, "Animation "..index.." does not exist.")
    end

    -- Set the animation
    SetPlayerAnimation(playerid, animID[index].name)

    AddPlayerChat(playerid, "Doing the animation! ( ID: "..index.." "..animID[index].name..")")
end
AddCommand("a", cmd_a)
